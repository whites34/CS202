/* switch statements */

const grade = 'F';

switch(grade){
    case 'A':
        console.log('you got an A');
        break;
    case 'B':
        console.log('You got a B');
        break;
    case 'C':
        console.log('you got a C');
        break;
    case 'D':
        console.log('you got a D');
        break;

    default:
        console.log('you got a lower grade');
}